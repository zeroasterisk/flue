/** Node.js build plugin. Produces a server for workflow runs and agent interactions. */
import { generateBuiltModuleNormalizationSource } from './generated-entry-normalization.ts';
import type { BuildContext, BuildPlugin } from './types.ts';

export class NodePlugin implements BuildPlugin {
	name = 'node';
	bundle: BuildPlugin['bundle'] = 'vite';

	generateEntryPoint(ctx: BuildContext): string {
		const { agents, appEntry, dbEntry, workflows } = ctx;
		const runtimeVersion = JSON.stringify(ctx.runtimeVersion);

		const agentImports = agents
			.map((a, index) => {
				const varName = agentVarName(a.name, index);
				return `import * as ${varName} from ${JSON.stringify(a.filePath.replace(/\\/g, '/'))};`;
			})
			.join('\n');

		const workflowImports = workflows
			.map((workflow, index) => {
				const varName = workflowVarName(workflow.name, index);
				return `import * as ${varName} from ${JSON.stringify(workflow.filePath.replace(/\\/g, '/'))};`;
			})
			.join('\n');

		const agentModuleEntries = agents
			.map((a, index) => `  ${JSON.stringify(a.name)}: ${agentVarName(a.name, index)},`)
			.join('\n');
		const workflowModuleEntries = workflows
			.map(
				(workflow, index) =>
					`  ${JSON.stringify(workflow.name)}: ${workflowVarName(workflow.name, index)},`,
			)
			.join('\n');

		// User-supplied app.ts (if any). The generated entry imports the user's
		// default export and dispatches all requests through `app.fetch`. When
		// no app.ts is present, the generated entry constructs a thin default
		// Hono that mounts `flue()` and renders canonical error envelopes.
		const userAppImport = appEntry ? `import userApp from ${JSON.stringify(appEntry.replace(/\\/g, '/'))};` : '';
		const userDbImport = dbEntry ? `import userPersistenceAdapter from ${JSON.stringify(dbEntry.replace(/\\/g, '/'))};` : '';

		// All HTTP routing, workflow admission, agent dispatch, and error
		// rendering live in @flue/runtime's runtime modules. The generated
		// entry's job is narrow: import handlers, build the runtime config,
		// instantiate sandboxes, start the listener.
		const packagedSkillsImport = `import { getPackagedSkills } from 'virtual:flue/packaged-skills';`;
		const packagedSkillsValue = 'getPackagedSkills()';
		const builtModuleNormalizationSource = generateBuiltModuleNormalizationSource();
		return `
// Auto-generated by flue (target: node)
${packagedSkillsImport}
import { serve } from '@hono/node-server';
import { sqlite } from '@flue/runtime/node';
import {
  Bash,
  InMemoryFs,
  createFlueContext,
  createNodeAgentCoordinator,
  createNodeDispatchQueue,
  bashFactoryToSessionEnv,
  resolveModel,
  configureFlueRuntime,
  createDefaultFlueApp,
  invokeWorkflowAttached,
  invokeDirectAttached,
  generateWorkflowRunId,
} from '@flue/runtime/internal';
${agentImports}
${workflowImports}
${userAppImport}
${userDbImport}

// ─── Config ─────────────────────────────────────────────────────────────────

const skills = {};
const packagedSkills = ${packagedSkillsValue};
const systemPrompt = '';

${builtModuleNormalizationSource}
const agentModules = {
${agentModuleEntries}
};
const workflowModules = {
${workflowModuleEntries}
};
const normalized = normalizeBuiltModules(agentModules, workflowModules);
const { manifest, createdAgents, dispatchAgentNames, workflowHandlers, localWorkflowHandlers, agentRouteMiddleware, workflowRouteMiddleware } = normalized;

const isLocalMode = process.env.FLUE_MODE === 'local';
const localCliTarget = process.env.FLUE_CLI_TARGET;
const localCliName = process.env.FLUE_CLI_NAME;
const localCliId = process.env.FLUE_CLI_ID;
const isLocalCliMode = localCliTarget !== undefined || localCliName !== undefined || localCliId !== undefined;

// ─── Sandbox Environments ───────────────────────────────────────────────────

/**
 * Create an empty in-memory sandbox (default).
 * Uses InMemoryFs (no real filesystem access) with sensible defaults:
 * cwd = /home/user, /tmp exists, /bin and /usr/bin exist.
 */
async function createDefaultEnv() {
  const fs = new InMemoryFs();
  return bashFactoryToSessionEnv(() => new Bash({
    fs,
    network: { dangerouslyAllowFullInternetAccess: true },
  }));
}

${
			dbEntry
				? `// Custom persistence from db.ts.
if (!userPersistenceAdapter || typeof userPersistenceAdapter.connect !== 'function') {
  throw new Error('[flue] db.ts must default-export a PersistenceAdapter with a connect() method.');
}
let executionStore;
let runStore;
let runRegistry;
let eventStreamStore;
try {
  if (userPersistenceAdapter.migrate) await userPersistenceAdapter.migrate();
  executionStore = userPersistenceAdapter.connect();
  if (!executionStore || typeof executionStore.sessions?.save !== 'function' || typeof executionStore.submissions?.getSubmission !== 'function') {
    throw new Error('connect() must return an AgentExecutionStore with sessions and submissions.');
  }
  runStore = userPersistenceAdapter.connectRunStore();
  runRegistry = userPersistenceAdapter.connectRunRegistry();
  if (typeof userPersistenceAdapter.connectEventStreamStore !== 'function') {
    throw new Error('connectEventStreamStore() must be defined on the PersistenceAdapter.');
  }
  eventStreamStore = userPersistenceAdapter.connectEventStreamStore();
  if (!eventStreamStore || typeof eventStreamStore.appendEvent !== 'function' || typeof eventStreamStore.readEvents !== 'function') {
    throw new Error('connectEventStreamStore() must return an EventStreamStore.');
  }
} catch (error) {
  throw new Error('[flue] Failed to initialize persistence from db.ts: ' + (error instanceof Error ? error.message : error), { cause: error });
}`
				: `// Default persistence for Node — in-memory SQLite, process lifetime.
const defaultAdapter = sqlite();
if (defaultAdapter.migrate) defaultAdapter.migrate();
const executionStore = defaultAdapter.connect();
const runStore = defaultAdapter.connectRunStore();
const runRegistry = defaultAdapter.connectRunRegistry();
const eventStreamStore = defaultAdapter.connectEventStreamStore();`
		}
const persistenceAdapter = ${dbEntry ? `userPersistenceAdapter` : `defaultAdapter`};
const agentCoordinator = createNodeAgentCoordinator({
  submissions: executionStore.submissions,
  agents: createdAgents,
  createContext: createContextForRequest,
  eventStreamStore,
});
const dispatchQueue = createNodeDispatchQueue(agentCoordinator);

// Build per-agent durable admission factories so HTTP prompts enter
// the same durable submission lifecycle as dispatches.
const createAdmission = Object.fromEntries(
  Object.keys(createdAgents).map((name) => [
    name,
    (instanceId) => agentCoordinator.createAdmission(name, instanceId),
  ]),
);

function createContextForRequest(id, runId, payload, req, initialEventIndex, dispatchId) {
  return createFlueContext({
    id,
    runId,
    dispatchId,
    payload,
    initialEventIndex,
    env: process.env,
    req,
    agentConfig: {
      systemPrompt, skills, packagedSkills, model: undefined, resolveModel,
    },
    createDefaultEnv,
    defaultStore: executionStore.sessions,
    submissionStore: executionStore.submissions,
  });
}

// ─── Runtime seed ───────────────────────────────────────────────────────────

// Seed the public flue() sub-app with everything it needs to dispatch agent
// requests in-process. Must run before \`flue()\` handles any request — by
// virtue of being a top-level statement, it executes before \`serve(...)\`
// below binds the listener. User app.ts files that call \`flue()\` at top
// level are also fine because Hono routes are lazy: they read this config
// only when a request arrives.
configureFlueRuntime({
  target: 'node',
  devMode: isLocalMode,
  runtimeVersion: ${runtimeVersion},
  manifest,
  createAdmission,
  dispatchQueue,
  resolveDispatchAgentName: (agent) => dispatchAgentNames.get(agent),
  workflowHandlers,
  agentRouteMiddleware,
  workflowRouteMiddleware,
  createContext: createContextForRequest,
  runStore,
  runRegistry,
  eventStreamStore,
});

// Reconcile any interrupted submissions from a previous process.
// This is best-effort on startup — errors are logged but do not block the server.
agentCoordinator.reconcileSubmissions().catch((error) => {
  console.error('[flue] Startup submission reconciliation failed:', error);
});

// ─── App composition ────────────────────────────────────────────────────────

${
	appEntry
		? `// User-supplied app.ts: their default export owns the entire request
// pipeline. We just verify it exposes a fetch method and pass the
// listener through. flue() is available for them to mount, but the
// composition is theirs to author.
const flueApp = userApp;
if (!flueApp || typeof flueApp.fetch !== 'function') {
  throw new Error(
    '[flue] app.ts default export must be a Hono app or an object with a fetch(request) method.'
  );
}`
		: `// No app.ts: build the default app via @flue/runtime so the generated entry
// stays \`hono\`-free (users only need hono in their node_modules when
// they author their own app.ts). The default mounts \`flue()\` at root
// and renders canonical Flue envelopes for unmatched paths.
const flueApp = createDefaultFlueApp();`
}

// ─── Start ──────────────────────────────────────────────────────────────────

function sendLocalMessage(message, done) {
  if (!process.send) throw new Error('[flue] Local CLI execution requires an inherited IPC connection.');
  process.send(message, done);
}

function localRequest() {
  return new Request('https://flue.invalid/_cli', { method: 'POST' });
}

function localErrorMessage(reason, requestId) {
  return { type: 'error', requestId, error: { type: 'invalid_request', message: reason, details: reason } };
}

function failLocalStartup(reason) {
  sendLocalMessage(localErrorMessage(reason), () => process.exit(1));
}

function parseIpcWorkflowMessage(raw) {
  if (!raw || typeof raw !== 'object' || raw.type !== 'invoke' || typeof raw.requestId !== 'string') {
    throw new Error('IPC workflow messages must have type "invoke" and a string requestId.');
  }
  return { type: 'invoke', requestId: raw.requestId, payload: raw.payload === undefined ? {} : raw.payload };
}

function parseIpcAgentMessage(raw) {
  if (!raw || typeof raw !== 'object' || typeof raw.requestId !== 'string') {
    throw new Error('IPC agent messages must have a string requestId.');
  }
  if (raw.type !== 'prompt' || typeof raw.message !== 'string') {
    throw new Error('IPC agent messages must have type "prompt" with a string message.');
  }
  return { type: 'prompt', requestId: raw.requestId, message: raw.message };
}

function ipcErrorMessage(error, requestId, runId) {
  const publicError = error instanceof Error
    ? { type: 'internal_error', message: error.message, details: error.message }
    : { type: 'internal_error', message: String(error), details: String(error) };
  return runId === undefined
    ? { type: 'error', requestId, error: publicError }
    : { type: 'error', requestId, runId, error: publicError };
}

function startLocalWorkflow(name) {
  const handler = localWorkflowHandlers[name];
  if (!handler) {
    failLocalStartup('Unknown workflow: ' + name);
    return;
  }
  let invoked = false;
  sendLocalMessage({ type: 'ready', target: 'workflow', name });
  process.on('message', (raw) => {
    let message;
    try {
      message = parseIpcWorkflowMessage(raw);
      if (invoked) {
        sendLocalMessage(localErrorMessage('Local workflow execution accepts one invocation only.', message.requestId));
        return;
      }
      invoked = true;
    } catch (error) {
      sendLocalMessage(ipcErrorMessage(error));
      return;
    }
    const runId = generateWorkflowRunId(name);
    sendLocalMessage({ type: 'started', requestId: message.requestId, runId });
    void invokeWorkflowAttached({
      owner: { kind: 'workflow', workflowName: name, instanceId: runId },
      id: runId,
      runId,
      payload: message.payload,
      request: localRequest(),
      handler,
      createContext: createContextForRequest,
      onEvent: (event) => sendLocalMessage({ type: 'event', requestId: message.requestId, runId, event }),
      runStore,
      runRegistry,
      eventStreamStore,
    }).then(
      (invocation) => sendLocalMessage({ type: 'result', requestId: message.requestId, runId, result: invocation.result ?? null }, () => process.exit(0)),
      (error) => sendLocalMessage(ipcErrorMessage(error, message.requestId, runId), () => process.exit(1)),
    );
  });
}

function startLocalAgent(name, id) {
  if (!id) {
    failLocalStartup('Local agent connection requires an instance id.');
    return;
  }
  if (!createAdmission[name]) {
    failLocalStartup('Unknown agent for admission: ' + name);
    return;
  }
  sendLocalMessage({ type: 'ready', target: 'agent', name, instanceId: id });
  process.on('message', (raw) => {
    let message;
    try {
      message = parseIpcAgentMessage(raw);
    } catch (error) {
      sendLocalMessage(ipcErrorMessage(error));
      return;
    }
    let didStart = false;
    void invokeDirectAttached({
      id,
      payload: { message: message.message },
      admitAttachedSubmission: createAdmission[name](id),
      onEvent: (event) => {
        if (!didStart) {
          didStart = true;
          sendLocalMessage({ type: 'started', requestId: message.requestId });
        }
        sendLocalMessage({ type: 'event', requestId: message.requestId, event });
      },
    }).then(
      (result) => sendLocalMessage({ type: 'result', requestId: message.requestId, result: result ?? null }),
      (error) => sendLocalMessage(ipcErrorMessage(error, message.requestId)),
    );
  });
}

if (isLocalCliMode) {
  if (typeof process.send !== 'function') {
    throw new Error('[flue] Local CLI execution requires an inherited IPC connection.');
  }
  if (!localCliName || (localCliTarget !== 'workflow' && localCliTarget !== 'agent')) {
    failLocalStartup('Invalid local CLI target configuration.');
  } else if (localCliTarget === 'workflow') {
    startLocalWorkflow(localCliName);
  } else {
    startLocalAgent(localCliName, localCliId);
  }
  process.on('disconnect', async () => {
    await agentCoordinator.shutdown();
    if (persistenceAdapter.close) await persistenceAdapter.close();
    process.exit(0);
  });
} else {
  const port = parseInt(process.env.PORT || '3000', 10);
  const server = serve({
    fetch: (request, env) => flueApp.fetch(request, env),
    port,
    serverOptions: { requestTimeout: 0 },
  });
  console.log('[flue] Server listening on http://localhost:' + port);
  if (isLocalMode) {
    console.log('[flue] Mode: local');
    console.log('[flue] Agents: ' + ${JSON.stringify(agents.map((a) => a.name).join(', '))});
  } else {
    console.log('[flue] Agents: ' + ${JSON.stringify(agents.map((a) => a.name).join(', '))});
  }
  let shuttingDown = false;
  async function stop(signal, exitCode) {
    if (shuttingDown) return;
    shuttingDown = true;
    console.error('[flue] Received ' + signal + ', shutting down...');
    // 1. Drain active submissions (abort at turn boundary, wait with timeout).
    await agentCoordinator.shutdown();
    // 2. Close persistence adapter.
    if (persistenceAdapter.close) await persistenceAdapter.close();
    // 3. Close HTTP server.
    await new Promise((resolve) => server.close(resolve));
    console.error('[flue] Stopped.');
    process.exit(exitCode);
  }
  process.on('SIGINT', () => { void stop('SIGINT', 130); });
  process.on('SIGTERM', () => { void stop('SIGTERM', 143); });
}
`;
	}

	external = ['node-liblzma', '@mongodb-js/zstd'];
}

function agentVarName(name: string, index: number): string {
	const readableName = name.replace(/[^a-zA-Z0-9]/g, '_').replace(/^_+|_+$/g, '') || 'agent';
	return `handler_${readableName}_${index}`;
}

function workflowVarName(name: string, index: number): string {
	const readableName = name.replace(/[^a-zA-Z0-9]/g, '_').replace(/^_+|_+$/g, '') || 'workflow';
	return `workflow_${readableName}_${index}`;
}
